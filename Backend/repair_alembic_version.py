"""Repair alembic_version tables that reference revisions whose files are gone.

Happens when a revision is autogenerated and applied on a box, then the file is
deleted/never committed: every alembic command fails with
"Can't locate revision identified by '<id>'".

Dry-run by default — prints each schema's stored version and what would change.
Run with --apply to repoint orphaned versions at the current head. The schema
contents are left untouched (whatever the lost revision did is already applied);
afterwards run gen_migration.py and review the diff it produces.
"""
import argparse
import asyncio

from sqlalchemy import text

from alembic.config import Config
from alembic.script import ScriptDirectory

from gen_migration import BACKEND_DIR
from app.database import engine


def known_revisions() -> tuple[set[str], str]:
    cfg = Config(str(BACKEND_DIR / "alembic.ini"))
    cfg.set_main_option("script_location", str(BACKEND_DIR / "alembic"))
    script_dir = ScriptDirectory.from_config(cfg)
    revisions = {s.revision for s in script_dir.walk_revisions()}
    head = script_dir.get_current_head()
    return revisions, head


async def repair(apply: bool) -> None:
    revisions, head = known_revisions()
    print(f"Current head in alembic/versions: {head}")

    async with engine.begin() as conn:
        schemas = (
            await conn.execute(
                text(
                    "SELECT table_schema FROM information_schema.tables "
                    "WHERE table_name = 'alembic_version' ORDER BY 1"
                )
            )
        ).scalars().all()

        if not schemas:
            print("No alembic_version tables found.")
            return

        for schema in schemas:
            stored = (
                await conn.execute(
                    text(f'SELECT version_num FROM "{schema}".alembic_version')
                )
            ).scalars().all()
            orphans = [v for v in stored if v not in revisions]
            if not orphans:
                print(f"{schema}: {stored} — OK")
                continue
            if apply:
                await conn.execute(
                    text(f'DELETE FROM "{schema}".alembic_version')
                )
                await conn.execute(
                    text(f'INSERT INTO "{schema}".alembic_version (version_num) VALUES (:h)'),
                    {"h": head},
                )
                print(f"{schema}: {stored} — orphaned {orphans}, re-stamped to {head}")
            else:
                print(f"{schema}: {stored} — ORPHANED {orphans}, would re-stamp to {head}")

    await engine.dispose()
    if not apply:
        print("\nDry run. Re-run with --apply to fix.")


def main():
    parser = argparse.ArgumentParser(description=__doc__.splitlines()[0])
    parser.add_argument("--apply", action="store_true", help="Write the fix (default: dry run)")
    args = parser.parse_args()
    asyncio.run(repair(args.apply))


if __name__ == "__main__":
    main()
